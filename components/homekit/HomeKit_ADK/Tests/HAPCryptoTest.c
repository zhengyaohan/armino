void test_chacha20_poly1305() {
    uint8_t t[CHACHA20_POLY1305_TAG_BYTES];
    uint8_t c[300];
    HAPRawBufferZero(c, sizeof c);
    size_t pt_len = sizeof chacha20_poly1305_pt - 1;
    HAP_chacha20_poly1305_encrypt_aad(
            t,
            c,
            chacha20_poly1305_pt,
            pt_len,
            chacha20_poly1305_aad,
            sizeof chacha20_poly1305_aad,
            chacha20_poly1305_nonce,
            sizeof chacha20_poly1305_nonce,
            chacha20_poly1305_key);
    HAPAssert(!memcmp(c, chacha20_poly1305_ct, sizeof chacha20_poly1305_ct));
    HAPAssert(!memcmp(t, chacha20_poly1305_tag, sizeof chacha20_poly1305_tag));
    uint8_t m[300];
    int ret = HAP_chacha20_poly1305_decrypt_aad(
            chacha20_poly1305_tag,
            m,
            chacha20_poly1305_ct,
            sizeof chacha20_poly1305_ct,
            chacha20_poly1305_aad,
            sizeof chacha20_poly1305_aad,
            chacha20_poly1305_nonce,
            sizeof chacha20_poly1305_nonce,
            chacha20_poly1305_key);
    HAPAssert(!ret);
    HAPAssert(!memcmp(m, chacha20_poly1305_pt, pt_len));
    c[0] ^= 1;
    ret = HAP_chacha20_poly1305_decrypt_aad(
            chacha20_poly1305_tag,
            m,
            c,
            sizeof chacha20_poly1305_ct,
            chacha20_poly1305_aad,
            sizeof chacha20_poly1305_aad,
            chacha20_poly1305_nonce,
            sizeof chacha20_poly1305_nonce,
            chacha20_poly1305_key);
    HAPAssert(ret == -1);
    t[0] ^= 1;
    ret = HAP_chacha20_poly1305_decrypt_aad(
            t,
            m,
            chacha20_poly1305_ct,
            sizeof chacha20_poly1305_ct,
            chacha20_poly1305_aad,
            sizeof chacha20_poly1305_aad,
            chacha20_poly1305_nonce,
            sizeof chacha20_poly1305_nonce,
            chacha20_poly1305_key);
    HAPAssert(ret == -1);
}

void test_chacha20_poly1305_inc() {
    uint8_t t[CHACHA20_POLY1305_TAG_BYTES];
    uint8_t c[300];
    size_t pt_len = sizeof chacha20_poly1305_pt - 1;
    uint8_t m[300];
    int ret;
    HAP_chacha20_poly1305_ctx ctx;
    HAP_chacha20_poly1305_init(&ctx, chacha20_poly1305_nonce, sizeof chacha20_poly1305_nonce, chacha20_poly1305_key);
    HAP_chacha20_poly1305_update_enc_aad(
            &ctx,
            chacha20_poly1305_aad,
            1,
            chacha20_poly1305_nonce,
            sizeof chacha20_poly1305_nonce,
            chacha20_poly1305_key);
    HAP_chacha20_poly1305_update_enc_aad(
            &ctx,
            chacha20_poly1305_aad + 1,
            sizeof chacha20_poly1305_aad - 1,
            chacha20_poly1305_nonce,
            sizeof chacha20_poly1305_nonce,
            chacha20_poly1305_key);
    for (size_t i = 0; i < pt_len; ++i) {
        HAP_chacha20_poly1305_update_enc(
                &ctx,
                c + i,
                chacha20_poly1305_pt + i,
                1,
                chacha20_poly1305_nonce,
                sizeof chacha20_poly1305_nonce,
                chacha20_poly1305_key);
    }
    HAP_chacha20_poly1305_final_enc(&ctx, t);
    HAPAssert(!memcmp(c, chacha20_poly1305_ct, sizeof chacha20_poly1305_ct));
    HAPAssert(!memcmp(t, chacha20_poly1305_tag, sizeof chacha20_poly1305_tag));
    HAP_chacha20_poly1305_init(&ctx, chacha20_poly1305_nonce, sizeof chacha20_poly1305_nonce, chacha20_poly1305_key);
    HAP_chacha20_poly1305_update_dec_aad(
            &ctx,
            chacha20_poly1305_aad,
            sizeof chacha20_poly1305_aad,
            chacha20_poly1305_nonce,
            sizeof chacha20_poly1305_nonce,
            chacha20_poly1305_key);
    for (size_t i = 0; i < sizeof chacha20_poly1305_ct; ++i) {
        HAP_chacha20_poly1305_update_dec(
                &ctx,
                m + i,
                chacha20_poly1305_ct + i,
                1,
                chacha20_poly1305_nonce,
                sizeof chacha20_poly1305_nonce,
                chacha20_poly1305_key);
    }
    ret = HAP_chacha20_poly1305_final_dec(&ctx, t);
    HAPAssert(!ret);
    HAPAssert(!memcmp(m, chacha20_poly1305_pt, sizeof chacha20_poly1305_ct));
    HAPAssert(!memcmp(t, chacha20_poly1305_tag, sizeof chacha20_poly1305_tag));
}

