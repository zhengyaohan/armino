/*----------------------------------------------------------------------------
 * da_securedebug_main
 *
 * Handle command line arguments if any, call produce random.
 */
int
da_securedebug_main(
        int argc,
        char * argv[])
{
    int i;
    int MainReturnCode = 0;

    if (Driver130_Init() < 0)
    {
        LOG_CRIT(DA_PREFIX "FAILED: Driver130 initialization\n");
        return -1;
    }

    // Parse command line arguments.
    gl_Program = argv[0];
    for (i = 1; i < argc; i++)
    {
        // Check if argument is option
        MainReturnCode = HandleOption(argv[i]);
        if (MainReturnCode != 0)
        {
            break;
        }
    }

    if (MainReturnCode == 0)
    {
        MainReturnCode = da_securedebug_module_init();
        da_securedebug_module_exit();
    }
    else if (MainReturnCode > 0)
    {
        MainReturnCode = 0;
    }

    Driver130_Exit();

    return MainReturnCode;
}


